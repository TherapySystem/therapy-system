const serviceActivities = {
    'Occupational Therapy': occupational,
    'Shadow Class': shadow,
    'Speech Therapy': speech,
    'Developmental Class': developmental
};

const getServiceVariables = (service) => {
    if (service == 'Occupational Therapy') {
        return ['Gross Motor Increase', 'Sensory Integration Skills', 'Fine Motor Skills', 'Play Skills'];
    } else if (service == 'Shadow Class') {
        return ['Gross Motor Increase', 'Social Interaction Skills', 'Sensory Integration Skills', 'Play Skills', 'Activities of Daily Living'];
    } else if (service == 'Speech Therapy') {
        return ['Language Comprehension (Receptive Language)', 'Speech Production (Expressive Language)', 'Social Communication Skills', 'Cognitive Abilities', 'Speech Fluency'];
    } else if (service == 'Developmental Class') {
        return ['Reading and Writing Skills', 'Social Interaction Skills', 'Math Skills'];
    }
};

const getRecommendedActivities = (service, variableScores) => {
    const variables = getServiceVariables(service);
    const recommendedActivities = {};
    for (var i = 0; i < variables.length; i++) {
        const variable = variables[i];
        const variableScore = variableScores[variable];
        recommendedActivities[variable] = serviceActivities[service][variable][variableScore];
    }

    return recommendedActivities;
};

